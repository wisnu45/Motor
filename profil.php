  <div id="judul">Profil Citra Motor Lubuklinggau</div>
  <p align="justify">Citra Motor  Lubuklinggau merupakan usaha mandiri yang bergerak  dalam bidang jual-beli kendaraan sepeda motor  yang berdiri pada tanggal 1 Mei 2006, yang terletak di Jl. Yos Sudarso Simpang  Periuk No. 09 Lubuklinggau, telp (0733) 451775.<br />
    <br />
Citra Motor Lubuklinggau  yang pada mulanya hanya mempunyai 10 unit sepeda motor, dan sekarang sudah berkembang  dan banyak menjual berbagai jenis produk  sepeda motor, bahkan sekarang Citra Motor Lubuklinggau sudah mempunyai cabang  yaitu di Curup dan di Merasi . Citra Motor Lubuklinggau yang bergerak di bidang  penjualann baik itu secara kredit maupun secara tunai dengan kerja sama pada  perusahaan peminjam atau Lising seperti, FIF, BAF , dan Adira Finance. Untuk  kendaraan yang di jual seperti Yamaha, Honda, Suzuki, Kawasaki dan sebagainya.<br />
<br />
  Visi dari Citra  Motor Lubuklinggau adalah memberikan pelayanan dan proses cepat dengan  mengedepankan profesionalisme. </p>
